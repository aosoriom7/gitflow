import numpy as np

# Funcion para sumar dos numeros
def suma(a,b):
    return a+b

# Funcion para restar dos numeros
def resta(a,b):
    return b-a

# Funcion para multiplicar dos numeros
def multiplicar(a,b):
    return a*b

# Funcion para dividir dos numeros
def dividir(a,b):
    return b/a

# Funcion para dividision entera dos numeros
def divEntera(a,b):
   return b//a

# Funcion para residuo de la division de dos numeros
def divResiduo(a,b):
    return b%a

# Funcion para calcular el cuadrado de un numero
def cuadrado(a):
    return a**2

 # Funcion para calcular la raiz de un numero
def raizcuad(a):
    return np.sqrt(a)

 # Funcion para calcular el logaritmo natural de un numero
def logaritmo(a):
    return np.log(a)